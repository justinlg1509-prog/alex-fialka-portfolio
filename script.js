// Scroll Reveal Animation
const revealElements = document.querySelectorAll("[data-reveal]");
const revealOnScroll = () => {
  revealElements.forEach((element) => {
    const elementTop = element.getBoundingClientRect().top;
    const windowHeight = window.innerHeight;
    if (elementTop < windowHeight - 100) {
      element.classList.add("active");
    }
  });
};

// Navbar Scroll Effect
const navbar = document.querySelector(".navbar");
const handleScroll = () => {
  if (window.scrollY > 50) {
    navbar.classList.add("scrolled");
  } else {
    navbar.classList.remove("scrolled");
  }
  revealOnScroll();
};

// Language Switcher Logic
const setLanguage = (lang) => {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[lang][key]) {
      // Handle different element types
      if (element.tagName === "IMG") {
        element.alt = translations[lang][key];
      } else if (element.tagName === "META") {
        element.content = translations[lang][key];
      } else if (key === "exp-more") {
        element.innerHTML = translations[lang][key];
      } else {
        element.textContent = translations[lang][key];
      }
    }
  });

  // Special case for Page Title
  if (translations[lang]["page-title"]) {
    document.title = translations[lang]["page-title"];
  }

  // Update active state for buttons
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });

  // Store preference
  localStorage.setItem("preferredLang", lang);
  document.documentElement.lang = lang;
};

// Event Listeners for Lang Buttons
document.querySelectorAll(".lang-btn").forEach((btn) => {
  btn.addEventListener("click", () => {
    setLanguage(btn.getAttribute("data-lang"));
    // Close mobile menu if open
    if (mobileMenu.classList.contains("active")) {
      toggleMenu();
    }
  });
});

// Hamburger & Mobile Menu
const hamburger = document.querySelector(".hamburger");
const mobileMenu = document.querySelector(".mobile-menu");
const body = document.body;

const toggleMenu = () => {
  hamburger.classList.toggle("active-hamburger");
  mobileMenu.classList.toggle("active");
  body.classList.toggle("no-scroll");
};

hamburger.addEventListener("click", toggleMenu);

// Close menu on link click
document.querySelectorAll(".mobile-nav-links a").forEach((link) => {
  link.addEventListener("click", toggleMenu);
});

// Smooth scrolling
document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", function (e) {
    e.preventDefault();
    const target = document.querySelector(this.getAttribute("href"));
    if (target) {
      target.scrollIntoView({ behavior: "smooth" });
    }
  });
});

// Final Initialization
window.addEventListener("resize", () => {
  if (window.innerWidth > 992 && mobileMenu.classList.contains("active")) {
    toggleMenu();
  }
});

// Contact Form Handling
const contactForm = document.getElementById("contact-form");
const formStatus = document.getElementById("form-status");

if (contactForm) {
  contactForm.addEventListener("submit", (e) => {
    e.preventDefault();
    const currentLang = localStorage.getItem("preferredLang") || "es";

    // Simulate sending
    const submitBtn = contactForm.querySelector('button[type="submit"]');
    const originalContent = submitBtn.innerHTML;
    submitBtn.disabled = true;
    submitBtn.innerHTML = '<i class="fas fa-spinner fa-spin"></i>';

    setTimeout(() => {
      formStatus.textContent = translations[currentLang]["form-success"];
      formStatus.style.color = "var(--primary-bright)";
      contactForm.reset();
      submitBtn.disabled = false;
      submitBtn.innerHTML = originalContent;

      setTimeout(() => {
        formStatus.textContent = "";
      }, 5000);
    }, 1500);
  });
}

// Hero Particles (Simple dynamic move)
const hero = document.getElementById("hero");
if (hero) {
  hero.addEventListener("mousemove", (e) => {
    const { clientX, clientY } = e;
    const { innerWidth, innerHeight } = window;
    const moveX = (clientX - innerWidth / 2) / 25;
    const moveY = (clientY - innerHeight / 2) / 25;
    hero.style.backgroundPosition = `calc(50% + ${moveX}px) calc(50% + ${moveY}px)`;
  });
}

